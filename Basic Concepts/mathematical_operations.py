#Exponentation
#Two asterisk ** are used to raise a number to the power of another,which is called exponentation
print(2**5)
print(2**3**2)#is same as 2**9
#if we want to see dividing two numbers as the integer we should use two slash //
print(10//2)
print(15//7)
#if we want to find only the remainder in a division,we use the modulo operator %
print(20%6)